package com.hepexta.interview;

public class BinaryShiftingExample {

    public static void main(String[] args) {
        int size = 1024;

        System.out.println(size);
        System.out.println(size/2);
        System.out.println(size >> 1);
        System.out.println(size/4);
        System.out.println(size >> 2);
    }
}
